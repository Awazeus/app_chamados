    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $regras = [
            'nome' => 'required|max:20',
            'descricao' => 'required|max:20'
        ];

        $feedback = [
            'nome.required' => 'O nome da categoria deve ser informado.',
            'nome.max' => 'O nome deve conter no máximo 20 caracteres.',
            'descricao.required' => 'A descrição da categoria deve ser informada.',
            'descricao.max' => 'A descrição deve conter no máximo 20 caracteres.'
        ];

        $request->validate($regras, $feedback);

        Categoria::create($request->all());
        return redirect()->route('categorias.index');
    }

